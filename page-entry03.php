<h3 class="entryTtl">ろ過成形作業</h3>
<p>□シリコーン樹脂内の不純物（ゴミ）を取り除き、梱包、出荷するお仕事になります。<br />
<br />
ろ過・成形業務<br />
1. 材料準備　電動フォークリフト（資格不要）で、ろ過機前に材料パレットを運搬<br />
2. ろ過機準備　ろ過専用の網をろ過機にセット<br />
3. 材料投入　ろ過機に準備した材料を投入<br />
4. 成形　ろ過機より搬出された製品を既定の大きさにカット<br />
5. 重量調整　カットした製品を重量機に乗せ、既定の重量に調整<br />
6. 製品排出　自動梱包機にラインで送られ、自動梱包後、出荷場へ搬送<br />
<br />
出荷・梱包業務<br />
1. 梱包　ラインで搬送された製品を段ボール自動梱包機で梱包<br />
2. 仕分　ロボットアームにより出荷パレットへ製品別に仕分し荷積み<br />
3. 出荷準備　荷積み完了後にフォークリフト（資格要）で運搬、ビニールで固定し、出荷</p>
